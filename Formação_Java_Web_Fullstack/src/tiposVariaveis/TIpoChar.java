package tiposVariaveis;

public class TIpoChar {

	/* Main é um método auto-executável em Java */
	public static void main(String[] args) {
		
		/* CHAR representa uma letra */
		char sexoFeminino = 'F';
		
		if(sexoFeminino == 'F') {
			System.out.println("Feminino");
		} else {
			System.out.println("Masculino");
		}
		
	}
	
}
